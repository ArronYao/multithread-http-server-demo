int open_listenfd(char* port){
    int listenfd, rc;
    addrinfo hints, *result, *p;

    memset(&hints, 0, sizeof(addrinfo));
    hints.ai_family = AF_INET;
    hints.ai_socktype = SOCK_STREAM;
    hints.ai_flags = AI_ADDRCONFIG | AI_PASSIVE | AI_NUMERICSERV;
    if ((rc = getaddrinfo(NULL, port, &hints, &result)) < 0){
        cerr << "open_listenfd: getaddrinfo failed." << endl;
        cerr << "Specific Information: " << gai_strerror(rc) << endl; 
        exit(0);
    }

    int optval = 1;

    for (p = result; p; p = p->ai_next){
        listenfd = socket(p->ai_family, p->ai_socktype, p->ai_protocol);
        if (listenfd < 0)  // failed to construct a socket, try the next
            continue;

        /* Eliminates "Address already in use" error from bind */
        setsockopt(listenfd, SOL_SOCKET, SO_REUSEADDR,    //line:netp:csapp:setsockopt
                (const void *)&optval , sizeof(int));

        if (bind(listenfd, p->ai_addr, p->ai_addrlen) == 0) //success
            break;

        if (close(listenfd) < 0){
            cerr << "open_listenfd: close listenfd failed." << strerror(errno) << endl;
            exit(0);
        }

    }

    freeaddrinfo(result);

    if (!p)
        return -1;
    else if (listen(listenfd, MAX_CONNECTIONS) < 0){
        Close(listenfd);
        return -1;
    }
    else
        return listenfd;    

}

